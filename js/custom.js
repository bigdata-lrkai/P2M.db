function getParameterByName(name, url) {
  if (!url) url = window.location.href;
  name = name.replace(/[\[\]]/g, '\\$&');
  var regex = new RegExp('[?&]' + name + '(=([^&#]*)|&|#|$)'),
    results = regex.exec(url);
  if (!results) return null;
  if (!results[2]) return '';
  return decodeURIComponent(results[2].replace(/\+/g, ' '));
}

   $(window).on("load", function(){
    var cancerValue = getParameterByName("cancer_type"); 
    var metValue = getParameterByName("metastatic_site"); 
    var dataValue = getParameterByName("accession"); 
    if(cancerValue !== null){
        $('#cancer').val(cancerValue);
    }
    if(metValue !== null){
        $('#siteset').val(metValue);
    }
    if(dataValue !== null){
        $('#dataset').val(dataValue);
        $('#gene_r').val(null);
        $('#gene').val(null);
    }
    $("#mySubmit").click();
    $('.loader-mask').fadeOut(); 
   });
